# Python first program to display a string

# This example stores data in a variable
name = "Raghav" # String (text)
age = 25 	# Integer (whole number)
height = 5.8	# Float (decimal number)
is_student = True # Boolean (True/False)

# Now let us display the variables
print("Hello, World!")
print(f"My name is {name} and I'm {age} years old")
print(f"My am {height} tall and I am a student {is_student}")

